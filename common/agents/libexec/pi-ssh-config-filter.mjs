#!/usr/bin/env node
import fs from "node:fs";
import os from "node:os";
import path from "node:path";

const DROP_DIRECTIVES = new Set([
  "usekeychain",
  "identityagent",
  "localcommand",
  "permitlocalcommand",
]);

function parseArgs(argv) {
  const args = { input: "", output: "", hostHome: os.homedir(), containerHome: "/root" };
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    const next = () => {
      if (i + 1 >= argv.length) throw new Error(`missing value for ${arg}`);
      return argv[++i];
    };
    if (arg === "--input") args.input = next();
    else if (arg === "--output") args.output = next();
    else if (arg === "--host-home") args.hostHome = next();
    else if (arg === "--container-home") args.containerHome = next();
    else throw new Error(`unknown argument: ${arg}`);
  }
  if (!args.input) throw new Error("--input is required");
  if (!args.output) throw new Error("--output is required");
  return args;
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function expandHome(value, hostHome) {
  if (value === "~") return hostHome;
  if (value.startsWith("~/")) return path.join(hostHome, value.slice(2));
  return value;
}

function globToRegExp(pattern) {
  let out = "^";
  for (const ch of pattern) {
    if (ch === "*") out += "[^/]*";
    else if (ch === "?") out += "[^/]";
    else out += escapeRegExp(ch);
  }
  return new RegExp(`${out}$`);
}

function expandIncludePattern(pattern, baseDir, hostHome) {
  let expanded = expandHome(pattern, hostHome);
  if (!path.isAbsolute(expanded)) expanded = path.join(baseDir, expanded);

  if (!/[?*]/.test(expanded)) return fs.existsSync(expanded) ? [expanded] : [];

  const dir = path.dirname(expanded);
  const namePattern = path.basename(expanded);
  if (!fs.existsSync(dir) || !fs.statSync(dir).isDirectory()) return [];

  const re = globToRegExp(namePattern);
  return fs.readdirSync(dir)
    .filter((name) => re.test(name))
    .map((name) => path.join(dir, name))
    .filter((file) => fs.existsSync(file) && fs.statSync(file).isFile())
    .sort();
}

function rewritePaths(line, hostHome, containerHome) {
  let out = line;
  out = out.replaceAll(hostHome, containerHome);
  out = out.replaceAll("$HOME", containerHome);
  out = out.replaceAll("${HOME}", containerHome);
  return out;
}

function firstToken(trimmed) {
  const match = trimmed.match(/^([^\s=]+)(?:\s+|\s*=|$)/);
  return match ? match[1].toLowerCase() : "";
}

function restAfterDirective(trimmed) {
  return trimmed.replace(/^[^\s=]+\s*=?\s*/, "").trim();
}

function processFile(file, options, seen, output) {
  const real = fs.realpathSync(file);
  if (seen.has(real)) {
    output.push(`# docker-pi: skipped recursive Include ${file}`);
    return;
  }
  seen.add(real);

  output.push(`# docker-pi: begin ${file.replace(options.hostHome, "~")}`);
  const content = fs.readFileSync(file, "utf8");
  const lines = content.split(/\r?\n/);
  const baseDir = path.dirname(file);

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) {
      output.push(line);
      continue;
    }

    const directive = firstToken(trimmed);
    if (!directive) {
      output.push(line);
      continue;
    }

    if (directive === "include") {
      const patterns = restAfterDirective(trimmed).split(/\s+/).filter(Boolean);
      let matched = 0;
      for (const pattern of patterns) {
        const files = expandIncludePattern(pattern, baseDir, options.hostHome);
        matched += files.length;
        for (const included of files) processFile(included, options, seen, output);
      }
      if (matched === 0) output.push(`# docker-pi: skipped unmatched Include ${restAfterDirective(trimmed)}`);
      continue;
    }

    if (DROP_DIRECTIVES.has(directive)) {
      output.push(`# docker-pi: dropped macOS-only or host-local option: ${directive}`);
      continue;
    }

    output.push(rewritePaths(line, options.hostHome, options.containerHome));
  }

  output.push(`# docker-pi: end ${file.replace(options.hostHome, "~")}`);
  seen.delete(real);
}

try {
  const args = parseArgs(process.argv.slice(2));
  const input = path.resolve(expandHome(args.input, args.hostHome));
  const outputPath = path.resolve(args.output);
  const output = [
    "# Generated by docker-pi from the host SSH config.",
    "# Do not edit: this file is recreated for each docker-pi run.",
  ];

  processFile(input, args, new Set(), output);

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, `${output.join("\n")}\n`, { mode: 0o600 });
} catch (error) {
  const message = error instanceof Error ? error.message : String(error);
  console.error(`pi-ssh-config-filter: ${message}`);
  process.exit(1);
}
